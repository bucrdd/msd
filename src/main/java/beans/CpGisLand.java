package beans;

public class CpGisLand {

	private String chrom;
	private int chromStart;
	private int chromEnd;
	private String name;
	private int length;
	private int cpgNum;
	private int gcNum;
	private float perCpg;
	private float perGc;
	private float obsExp;
	
	public String getChrom() {
		return chrom;
	}
	public void setChrom(String chrom) {
		this.chrom = chrom;
	}
	public int getChromStart() {
		return chromStart;
	}
	public void setChromStart(int chromStart) {
		this.chromStart = chromStart;
	}
	public int getChromEnd() {
		return chromEnd;
	}
	public void setChromEnd(int chromEnd) {
		this.chromEnd = chromEnd;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLength() {
		return length;
	}
	public void setLength(int length) {
		this.length = length;
	}
	public int getCpgNum() {
		return cpgNum;
	}
	public void setCpgNum(int cpgNum) {
		this.cpgNum = cpgNum;
	}
	public int getGcNum() {
		return gcNum;
	}
	public void setGcNum(int gcNum) {
		this.gcNum = gcNum;
	}
	public float getPerCpg() {
		return perCpg;
	}
	public void setPerCpg(float perCpg) {
		this.perCpg = perCpg;
	}
	public float getPerGc() {
		return perGc;
	}
	public void setPerGc(float perGc) {
		this.perGc = perGc;
	}
	public float getObsExp() {
		return obsExp;
	}
	public void setObsExp(float obsExp) {
		this.obsExp = obsExp;
	}
	
	
	
}
