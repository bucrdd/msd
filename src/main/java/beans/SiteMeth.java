package beans;

public class SiteMeth {
	private String chrom;
	private int start;
	private int end;
	private float methyValue;
	
	public String getChrom() {
		return chrom;
	}
	public void setChrom(String chrom) {
		this.chrom = chrom;
	}
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}
	public int getEnd() {
		return end;
	}
	public void setEnd(int end) {
		this.end = end;
	}
	public float getMethyValue() {
		return methyValue;
	}
	public void setMethyValue(float methyValue) {
		this.methyValue = methyValue;
	}
	
	
}
