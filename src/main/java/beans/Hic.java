package beans;

public class Hic {
	private String chrom;
	private int start;
	private int end;
	private float hicValue;
	
	public String getChrom() {
		return chrom;
	}
	public void setChrom(String chrom) {
		this.chrom = chrom;
	}
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}
	public int getEnd() {
		return end;
	}
	public void setEnd(int end) {
		this.end = end;
	}
	public float getHicValue() {
		return hicValue;
	}
	public void setHicValue(float hicValue) {
		this.hicValue = hicValue;
	}
}
