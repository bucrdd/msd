package beans;

public class SpCas9 {
	private String chrom;
	private int start;
	private int end;
	private String seedSeq;
	
	public String getChrom() {
		return chrom;
	}
	public void setChrom(String chrom) {
		this.chrom = chrom;
	}
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}
	public int getEnd() {
		return end;
	}
	public void setEnd(int end) {
		this.end = end;
	}
	public String getSeedSeq() {
		return seedSeq;
	}
	public void setSeedSeq(String seedSeq) {
		this.seedSeq = seedSeq;
	}
}
